public class Problem38DigitToWord {
    public static void main(String[] args) {

        int a = 1;
        int b = 2;

        if (a == 1) {
            System.out.println("L");
        }
        if (b == 2) {
            System.out.println("O");
        }
        if (a == 1) {
            System.out.println("L");
        }

    }
}
