public class Problem32StopLight {
    public static void main(String[] args) {

        String light = "yellow";

        if (light == "red") {
            System.out.println("Stop");
        }
        if (light == "green") {
            System.out.println("Go");
        }
        if (light == "yellow") {
            System.out.println("Ready");
        }

    }
}
