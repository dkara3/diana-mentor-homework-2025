public class Problem34NumberSign {
    public static void main(String[] args) {

        int n = 32;

        if (n == 0) {
            System.out.println("Zero");
        }
        if (n < 0) {
            System.out.println("Negative");
        }
        if (n > 0) {
            System.out.println("Positive");
        }

    }
}
