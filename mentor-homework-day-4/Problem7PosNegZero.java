public class Problem7PosNegZero {
    public static void main(String[] args) {

        int num = 0;

        if (num == 0) {
            System.out.println("Zero");
        }
        if (num > 0) {
            System.out.println("Positive");
        }
        if (num < 0) {
            System.out.println("Negative");
        }


    }
}
